#!/usr/bin/env python3

"""module that creates a function that concatenates two strings."""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings together

    Args:
        str1 (str): First string.
        str2 (str): Second string.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
